def area(a):
    '''Accepts number a - the length of the side of the square,
    returns the area of a square with side a.
        Example: area(6) -> 36'''
    return a * a


def perimeter(a):
    '''Accepts number a - the length of the side of the square,
    returns the perimeter of a square with side a.
        Example: perimeter(6) -> 24'''
    return 4 * a
