def area(a, b):
    '''Принимает на вход числа a и b - длины соседних сторон прямоугольника,
    возвращает площадь прямоугольника со сторонами a и b.'''
    return a * b 


def perimeter(a, b):
    '''Принимает на вход числа a и b - длины соседних сторон прямоугольника,
    возвращает периметр прямоугольника со сторонами a и b.'''
    return (a + b) * 2
