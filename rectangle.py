def area(a, b):
    '''Accepts numbers a and b - the lengths of sides of the rectangle,
    returns area of a rectangle with sides a and b.
        Example: area(3, 2) -> 6'''
    return a * b 


def perimeter(a, b):
    '''Accepts numbers a and b - the lengths of sides of the rectangle,
    returns the perimeter of a rectangle with sides a and b.
        Example: perimeter(3, 2) -> 10'''
    return (a + b) * 2
